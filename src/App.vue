<script>
import anime from 'animejs';
import Fullpage from 'fullpage.js';
import 'fullpage.js/dist/fullpage.css';

export default {
  name: 'App',
  data() {
    return {
      text: "HI I'm Zowie",
    };
  },
  mounted() {
    window.addEventListener('scroll', this.handleScroll);
    this.animateText();
    this.animateFirstSVG();
    // eslint-disable-next-line no-new
    new Fullpage('#fullpage', {
      autoScrolling: true,
      scrollHorizontally: true,
    });
  },
  beforeDestroy() {
    window.removeEventListener('scroll', this.handleScroll);
  },
  methods: {
    animateFirstSVG() {
      anime({
        targets: '#first-svg path',
        strokeDashoffset: [anime.setDashoffset, 0],
        easing: 'easeInOutSine',
        duration: 2000,
        delay: anime.stagger(100, { start: 400 }),
        endDelay: 400,
        direction: 'normal',
        loop: false,
        complete: () => {
          this.startSecondSVGAnimation();
        },
      });
    },
    startSecondSVGAnimation() {
      document.querySelector('#first-svg').style.opacity = 0;
      document.querySelector('#second-svg').style.opacity = 1;
      anime({
        targets: '#second-svg path',
        strokeDashoffset: [anime.setDashoffset, 0],
        easing: 'easeInOutSine',
        duration: 2000,
        delay: anime.stagger(100, { start: 400 }),
        direction: 'normal',
        loop: false,
      });
      setTimeout(() => {
        anime({
          targets: '.css-transforms-demo .el',
          translateX: [window.innerWidth, 1200], // 從視窗右邊外部移動到 1150px 的位置
          scale: 3, // 縮放到 2 倍
          rotate: '3turn', // 旋轉
          duration: 1000, // 動畫持續時間
          easing: 'easeInOutQuad', // 動畫的緩動效果
        });
      }, 2000);
    },
    animateText() {
      // Text animation code using anime.js
    },
    handleScroll() {
      const scrolled = window.scrollY;
      document.querySelectorAll('.parallax-content').forEach((el) => {
        const speed = el.getAttribute('data-speed') || 0.1;
        // eslint-disable-next-line no-param-reassign
        el.style.transform = `translateY(${scrolled * speed}px)`;
      });
    },
  },
};
</script>

